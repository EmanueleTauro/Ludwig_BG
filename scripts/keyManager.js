// Define keys for movement
const keys = {
  w: {
    pressed: false,
  },
  a: {
    pressed: false,
  },
  s: {
    pressed: false,
  },
  d: {
    pressed: false,
  },
};

// MOVEMENT CHARACTERISTICS
let lastKey = "";

// Key DOWN
window.addEventListener("keydown", (e) => {
  // Checks for interaction on space press.
  if (e.key === " ") {
    if (player.interactionAsset !== null) {
      performInteraction({ player });
    } else {document.querySelector("#interactionDialogueBox").style.visibility = "hidden"}
  }

  // Movement Switch-Case
  switch (e.key) {
    case "w":
      keys.w.pressed = true;
      lastKey = "w";
      break;
    case "a":
      keys.a.pressed = true;
      lastKey = "a";
      break;
    case "s":
      keys.s.pressed = true;
      lastKey = "s";
      break;
    case "d":
      keys.d.pressed = true;
      lastKey = "d";
      break;
  }
});

// Key UP
window.addEventListener("keyup", (e) => {
  switch (e.key) {
    case "w":
      keys.w.pressed = false;
      break;
    case "a":
      keys.a.pressed = false;
      break;
    case "s":
      keys.s.pressed = false;
      break;
    case "d":
      keys.d.pressed = false;
      break;
  }
});
